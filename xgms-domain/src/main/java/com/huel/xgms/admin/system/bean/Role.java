package com.huel.xgms.admin.system.bean;

public class Role {

	/**
	 * 主键
	 */
	private int id;
	
	/**
	 * 名称
	 */
	private String name;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
}
